# bin
# 2진법으로 변환
print(bin(10))

# oct
# 8진법으로 변환
print(oct(10))

# hexa
# 16진법
print(hex(16))

# int
# 10 진법으로
print(int(10))

# not(~) 비트 반전자!
print(bin(~0b0010))

# shift (<<, >> ) 이동!
print(bin(0b11<<3))
print(bin(0b11>>3))

# |
print(35|5)

